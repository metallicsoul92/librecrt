/* iso646.h */

#ifndef _ISO646_H
#define _ISO646_H

/**
 * @brief Defines alternative spellings for common operators.
 *
 * This header provides macros that define alternative names for common C operators.
 */

#define and     &&  /**< Alternative spelling for logical AND */
#define and_eq  &=  /**< Alternative spelling for bitwise AND assignment */
#define bitand  &   /**< Alternative spelling for bitwise AND */
#define bitor   |   /**< Alternative spelling for bitwise OR */
#define compl   ~   /**< Alternative spelling for bitwise complement */
#define not     !   /**< Alternative spelling for logical NOT */
#define not_eq  !=  /**< Alternative spelling for inequality */
#define or      ||  /**< Alternative spelling for logical OR */
#define or_eq   |=  /**< Alternative spelling for bitwise OR assignment */
#define xor     ^   /**< Alternative spelling for bitwise XOR */
#define xor_eq  ^=  /**< Alternative spelling for bitwise XOR assignment */

#endif /* _ISO646_H */
